def area(length,width)
    return length*width
end

def speed(dist,time)
    return dist/time
end

def circumference(radius)
    return 2*3.14159*radius
end

def volume(radius)
    return 4.0/3*3.14159*(radius**3)
end

def hypotenuse(one,two)
    return Math.sqrt(one**2+two**2)
end

def energy(m)
    return m*299792458**2
end
